package com.kollus.vod.player;

public class PcSkin {
    String skin_path;
    String skin_sha1sum;
    public PcSkin(){}

    public String getSkin_path() {
        return skin_path;
    }

    public void setSkin_path(String skin_path) {
        this.skin_path = skin_path;
    }

    public String getSkin_sha1sum() {
        return skin_sha1sum;
    }

    public void setSkin_sha1sum(String skin_sha1sum) {
        this.skin_sha1sum = skin_sha1sum;
    }
}
